/**
 * An interface representing a water-dwelling entity.
 */
public interface Water {
	/**
	 * Checks if the water-dwelling entity has gills.
	 *
	 * @return true if the entity has gills, false otherwise.
	 */
	boolean hasGills();

	/**
	 * Checks if the water-dwelling entity lays eggs.
	 *
	 * @return true if the entity lays eggs, false otherwise.
	 */
	boolean hasLaysEggs();

}
